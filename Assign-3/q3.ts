let personName = "Syed Munir Ullah";

// Uppercase in typescript
console.log(personName.toUpperCase());  //SYED MUNIR ULLAH

// Lowercase in typescript
console.log(personName.toLowerCase());  //syed munir ullah

// Titlecase in typescript
console.log(personName.charAt(0).toUpperCase() + personName.slice(1))    //Syed Munir Ullah

